    private void jLabel3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel3MouseClicked

        if (gpc.BuscarID_PersonaProyecto(txtID.getText()) == null) {
            JOptionPane.showMessageDialog(null, "CLIENTE NO ECONTRADO O NO HA REGISTRADO PROYECTO", "ERROR", JOptionPane.INFORMATION_MESSAGE);
        } else {
            ID_envio = gpc.BuscarID_PersonaProyecto(txtID.getText()).getCliente().getId();
            JOptionPane.showMessageDialog(
                    this,
                    "SE ENCONTRARON RESULTADOS PARA "
                    + gpc.BuscarID_PersonaProyecto(txtID.getText()).getCliente().getNombre().toUpperCase(),
                    "Information",
                    JOptionPane.INFORMATION_MESSAGE
            );
            dispose();
            Tabla_cliente clien = new Tabla_cliente();
            clien.setVisible(true);
        }

    }//GEN-LAST:event_jLabel3MouseClicked

